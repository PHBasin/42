#include "ft.h"

int		ft_atoi(char *str)
{
	int	neg;
	int	n;

	while ((*str > 8 && *str < 14) || *str == 32)
		str++;
	neg = 1;
	while (*str == 43 || *str == 45)
	{
		if (*str == 45)
			neg *= -1;
		str++;
	}
	n = 0;
	while (*str > 47 && *str < 58)
	{
		if (n != 0)
			n *= 10;
		n += (*str - 48);
		str++;
	}
	return (n * neg);
}
